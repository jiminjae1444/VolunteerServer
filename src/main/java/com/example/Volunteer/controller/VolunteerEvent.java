package com.example.Volunteer.controller;

public class VolunteerEvent {
    private String volunteerName;
    private String volunteerHour;
    private String volunteerPersons;

    public String getVolunteerName() {
        return volunteerName;
    }

    public void setVolunteerName(String volunteerName) {
        this.volunteerName = volunteerName;
    }

    public String getVolunteerHour() {
        return volunteerHour;
    }

    public void setVolunteerHour(String volunteerHour) {
        this.volunteerHour = volunteerHour;
    }

    public String getVolunteerPersons() {
        return volunteerPersons;
    }

    public void setVolunteerPersons(String volunteerPersons) {
        this.volunteerPersons = volunteerPersons;
    }
}
